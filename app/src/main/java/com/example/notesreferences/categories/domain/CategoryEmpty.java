package com.example.notesreferences.categories.domain;

public class CategoryEmpty {

    int id;
    String title;

    public CategoryEmpty(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
